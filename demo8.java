// Example for els-if lader
class demo8
{
	public static void main (String[] args)
	{
		System.out.println("Main Start");
		if (10!=10)
		{
			System.out.println("HI IF");
		}
		else if (10>10)
		{
			System.out.println("HI Else-IF");
		}
		else 
		{
			System.out.println("HI Else");
		}
		System.out.println("Main end");
	}
}